public class MyClass {

    public MyClass() {
        System.out.println("heloo");
    }

    public MyClass(String a) {
        System.out.println("hello wiht string parameter");
    }

    public MyClass(int a) {
        System.out.println("hello wiht string parameter");
    }

    public MyClass(double a) {
        System.out.println("hello wiht string parameter");
    }

    public MyClass(double a, int b) {
        System.out.println("hello wiht string parameter");
    }

    public static void h() {
        System.out.println("wihout any parameter");
    }

    public static void h(String a) {
        System.out.println("have string parameter");
    }

    public static void h(int a) {
        System.out.println("int parmater");
    }

    public static void h(double a) {
        System.out.println("double parameter");
    }
}
